function changeStyle(){
// Seleciona a div com o id "container" e alinha o texto ao centro
let myContainer = document.querySelector('#container');
myContainer.style.textAlign = 'center';

// Seleciona a imagem e aplica as propriedades de estilo (largura e border-radius)
let myImage = document.querySelector('img');
myImage.style.width = '150px';
myImage.style.borderRadius = '50%';
}